                    <h4>A. Program Afiliasi</h4>
                    <p>Affiliate program atau dalam Bahasa Indonesia biasa disebut dengan program afiliasi atau kemitraan adalah suatu teknik marketing dimana penjual bekerjasama dengan pemasar melalui website pemasar. Dalam prakteknya para pemasar mendapatkan link khusus yang sudah diberi tracking (pelacak) sehingga setiap transaksi yang dating dari pemasar akan diketahui oleh system afiliasi penjual. Jika penjualan memenuhi kriteria afiliasi maka pemasar akan mendapatkan komisi dari penjualan tersebut. Komisi penjualan biasanya berupa persentasi dari harga barang yang telah terjual.</p>
                    <p>Afiliasi marketing adalah salah satu usaha untuk dapat memperoleh uang di dunia internet atau boleh juga di sebut usaha untuk mencari peluang bisnis yang menghasilkan di dunia maya. AFILIASI MARKETING ini akan membayar kita bila kita dapat menjual barang atau pun jasa yang dari affiliate merchant (pemilik produk tersebut). </p>
                    <p>Jenis usaha ini semacam kita menjualkan barang orang lain agar mau membeli barang orang lain yang kita tawarkan tersebut melalui kita. Komisi dari bisnis ini bervariasi dari 10% sampai 50 % terkantung jenis yang kita bantu untuk menawarkannya. Bisnis ini kita tidak perlu berjualan langsung kepada konsumen kita hanya berinteraksi dengan konsumen dengan menggunakan komputer yang ada internetnya.</p>

                    <h5>Macam - Macam dari AFILIASI MARKETING adalah :</h5>
                    <p>1.   Pay Per click ( PPC )</p>
                    <p>Bayar per klik atau di sebut juga PPC adalah salah satu strategi dari pemasaran online yang power ful jika kita bandingkan dengan teknik pemasaran yang lain kenapa demikian karena dimana pemasang iklan hanya membayar jika ada konsumen yang mengklik iklan tersebut.</p>
                    <p>2.   Pay Per Sale ( PPS )</p>
                    <p>Pemasaran online model ini kita mendapatkan bayaran apa bila kita dapat menjual suatu barang atau jasa yang di tawarkan oleh salah satu perusahaan. dan sistem pembayarannya dengan sistem bagi hasil dan kita dapat persentase dari barang dan jasa yang kita bantu menjualkannya.
                    Dalam pemasaran online ini kita sebagai resellernya</p>
                    <p>3.   Pay Per Lead ( PPL )</p>
                    <p>Pay Per Lead atau ( PPL ) merupakan program yang akan membayar kita jika kita dapat mengajak orang lain untuk menjadi member dalam situs tersebut. Jadi semakin banyak orang â€“ orang yang akan kita ajak untuk bergabung menjadi member maka semakin banyak juga pendapatani yang akan kita terima.</p><br>

                    <h4>B.  Dropship</h4>
                    <p>dropshipping merupakan proses pengiriman barang secara langsung ke pelanggan Anda dari pemasok yang merupakan pihak ketiga atau â€œvendorâ€. Proses ini dilakukan tanpa perantara lainnya semacam menyimpan barang terlebih dahulu di lokasi tertentu milik Anda.</p>
                    <p>dropshipping hampir mirip seperti bisnis reseller. Pada dropshipper, pihak penjual tidak melakukan kegiatan menstok barang, dan penjual ini juga tidak juga ikut serta dalam proses pengiriman barangnya ke pemesan. Penjual ini cuma meneruskan pesanan dari pihak pembeli ke pihak supplier-nya. Dan nantinya, pihak supplier yang akan mengirim barang tersebut ke pihak pembeli atas nama si penjual tadi.</p>
                    <p>Contoh skema yang dapat mempermudah pemahaman apa itu kegiatan bisnis dropshipping.</p>
                    <p>Jika Anda menjadi seorang dropshipper, Anda tidak membutuhkan modal yang besar. Anda sebagai dropshipper hanya perlu modal kuota internet yang cukup, koneksi atau relasi dengan supllier-nya, dan tentunya Anda juga butuh media promosi barang jualannya di internet.</p>
                    <p>Seorang dropshipper juga tidak perlu membeli atau membayar terlebih dahulu untuk barang dagangannya. Dropshipper juga tidak perlu menstok barang untuk dijual kembali ke para pembelinya. Tetapi, seorang dropshipper perlu memajang berbagai konten untuk penjualannya di berbagai media online. Ada banyak sih online store besar yang sudah mendukung kegiatan dropshipping seperti Shoppee, Tokopedia dan lain-lain. Selain itu, Anda juga bisa memanfaatkan media sosial, blog dan website Anda sendiri.</p>
                    <p>Seorang dropshipper biasanya sudah diberikan berbagai konten oleh supplier (si pemasok barang). Jadi nantinya, berbagai konten tersebut dipromosikan oleh Anda sebagai seorang dropshipper dengan harga yang berbeda dari harga asli yang diberikan sebelumnya oleh pihak supplier tadi.</p>
                    <p>Jika nantinya ada pembeli yang tertarik dan beli, si dropshipper menghubungi supplier buat kirimkan barangnya tapi atas nama si dropshipper.</p><br>

                    <h4>D. Reseler</h4>
                    <h5>3 kategori Skala penjualan reseller </h5>
                    <p>1.   Distributor Atau Supplier</p>
                    <p>Merupakan pihak pertama yang menyediakan dan menjual barang atau jasa dengan harga relatif murah di bandingkan dengan harga yang ada di pasaran, sehingga jika ada orang yang ingin menjual kembali barang yang sudah di beli
                    akan mendapatkan sejumlah untung yang lumayan. Inilah yang kemudian di sebut reseller.
                    </p>
                    <h5>2.  Reseller</h5>
                    <p>Reseller adalah, orang yang membeli produk dari distributor atau supplier dengan harga yang lebih murah dari pasaran untuk di jual kebali dengan harapan mendapatkan sejumlah keuntungan dari penjulan barangnya tersebut.</p>
                    <h5>3.  Custumer</h5>
                    <p>Custumer adalah pembeli atau konsumen yang membeli langsung kepada reseller ataupun distributor.</p>

                    <h5>Perbedaan Reseller Dengan Dropshipper?</h5>
                    <p>Ya memang mirip banget yang membedakan reseller dan dropshipper itu hanya dari stock barang saja.</p>
                    <p>Kalau reseller kita wajib membeli dan punya stock barang kalau dropshipper gak, karena di dropshipper yang kita perlukan cuma katalog produk berupa foto-foto saja untuk kita tawarkan kepada custumer.</p>
