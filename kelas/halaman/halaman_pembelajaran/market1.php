                    <h4>A. Pengertian Domain</h4>
                    <p>Domain adalah nama unik yang diberikan untuk mengidentifikasi nama server komputer seperti web server atau email server di jaringan komputer ataupun internet. Fungsi Domain adalah untukmempermudah pengguna di internet pada saat melakukan akses ke server, selain juga dipakai untuk mengingat nama server yang dikunjungi tanpa harus mengenal deretan angka yang rumit yang dikenal sebagai IP address. Nama domain ini juga dikenal sebagai sebuah kesatuan dari sebuah situs web seperti contohnya "wikipedia.org". Nama domain kadang-kadang disebut pula dengan istilah URL, atau alamat website.</p>
                    <p>Contoh : misal alamat IP server tempat file web kita disimpan adalah 77.165.90.89 sebenarnya kita bisa saja mengetikkan 77.165.90.89 langsung di browser untuk mengakses website kita, namun hal ini tentunya sangat tidak baik bagi ingatan orang khususnya ingatan pengunjung website yang mengakses website kita. Sehingga fungsi domain lebih untuk mudahkan orang mengingat sebuah nama url dibanding deretan deretan angka IP. Untuk itu diperlukan domain guna mengganti deretan angka tersebut agar menjadi deretan kata yang tentunya gampang diingat. misal: websitepraktis.com akan lebih mudah diingat ketimbang angka 218.719.51.617
                    Sebuah nama domain biasanya terdiri dari dua bagian atau lebih (secara teknis disebut label), dipisahkan dengan titik.
                    </p>
                    <h5>a.  Top Level Domain</h5>
                    <p>Label paling kanan menyatakan top-level domain - domain tingkat atas/tinggi (misalkan, alamat www.wikipedia.org memiliki top-level domain org).
                    Setiap label di sebelah kirinya menyatakan sebuah sub-divisi atau subdomain dari domain yang lebih tinggi. Catatan: "subdomain" menyatakan ketergantungan relatif, bukan absolut. Contoh: wikipedia.orgmerupakan subdomain dari domain org, dan id.wikipedia.org dapat membentuk subdomain dari domain wikipedia.org (pada praktiknya, id.wikipedia.org
                     
                    sesungguhnya mewakili sebuah nama host - lihat dibawah). Secara teori, pembagian seperti ini dapat mencapai kedalaman 127 level, dan setiap label dapat terbentuk sampai dengan 63 karakter, selama total nama domain tidak melebihi panjang 255 karakter. Tetapi secara praktik, beberapa pendaftar nama domain (domain name registry) memiliki batas yang lebih sedikit.
                    Terakhir, bagian paling kiri dari bagian nama domain (biasanya) menyatakan nama host. Sisa dari nama domain menyatakan cara untuk membangun jalur logis untuk informasi yang dibutuhkan; nama host adalah tujuan sebenarnya dari nama sistem yang dicari alamat IP-nya. Contoh: nama domain www.wikipedia.org memiliki nama host "www".</p>
                    <img width="50%" src="assets/img/M.PNG"><br><br>
                    <h5>B. Domain Indonesia</h5>
                    <p>Untuk Indonesia terbagi menjadi beberapa sub domain seperti :</p>
                    <p>1.   ac.id : Jenis domain yang digunakan bagi lingkungan akademik/perguruan tinggi dengan ketentuan dan kebijakan yang telah ditentukan.</p>
                    <p>2.   co.id : Jenis domain yang digunakan bagi organisasi komersial yang pada ketentuan dan kebijakan selanjutnya hanya diperuntukan bagi perusahaan dengan ketentuan dan kebijakan yang telah ditentukan.</p>
                    <p>3.   or.id : Jenis domain yang digunakan bagi organisasi selain organisasi yang masuk kedalam kategori domain ac.id, co.id, net.id, go.id, mil.id, sch.id, dst dengan ketentuan dan kebijakan yang telah ditentukan.</p>
                    <p>4.   net.id : Jenis domain yang digunakan bagi organisasi pemegang Izin Penyelenggara jasa telekomunikasi dengan ketentuan dan kebijakan yang telah ditentukan.</p>
                    <p>5.   web.id : Jenis domain yang digunakan bagi personal dan organisasi dengan ketentuan dan kebijakan yang telah ditentukan.</p>
                    <p>6.   sch.id : Jenis domain yang digunakan bagi sekolah dengan ketentuan dan kebijakan yang telah ditentukan.</p>
                    <p>7.   go.id : Jenis domain yang digunakan khusus bagi instansi pemerintah dengan ketentuan dan kebijakan yang telah ditentukan.</p>
                    <p>8.   mil.id : Jenis domain yang digunakan bagi kalangan militer dengan ketentuan dan kebijakan yang telah ditentukan.</p>
                    <p>9.   war.net.id Jenis domain yang digunakan bagi warung internet dengan ketentuan dan kebijakan yang telah ditentukan.</p><br>

